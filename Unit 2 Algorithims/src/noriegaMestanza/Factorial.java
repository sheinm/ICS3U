public class Factorial {

	public static void main (String [] args){
		  
	  } 
	
	public static int  factorial (int number) {
		
		
		
		for (int multiply= number-1; multiply> 0; multiply-- ) {
			 number = number*multiply;		 
		}
		return number;
		

	
		
	}
	
}
